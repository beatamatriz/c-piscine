#include <unistd.h>

void	ft_putstr(char *str)
{
	while (*str != '\0')
	{
		write(1, str, 1);
		str++;
	}
	write(1, "\n", 1);
}

int	main(int argn, char *argv[])
{
	while (1 < argn--)
		ft_putstr(argv[argn]);
	return (0);
}
