#include <unistd.h>

void	ft_putstr(char *str)
{
	while (*str != '\0')
	{
		write(1, str, 1);
		str++;
	}
}

int	main(void)
{
	char	*mantra;

	mantra = "La respuesta es 42";
	ft_putstr(mantra);
	return (0);
}
